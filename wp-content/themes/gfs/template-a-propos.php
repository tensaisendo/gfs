A propos

GoFor Seduction, le site qui se veut rassembler le talent et l'effort du meilleur de la séduction.

Notre but est de créer un point central où tous les amateurs de la séduction, qu'ils soient débutants ou expérimentés, peuvent venir trouver inspiration : un espace pour discuter, un lieu où de rassemblement
de connaissances et expériences, donner et recevoir des critiques respectueuses et constructives.

Notre mission est de rassembler les techniques les plus utiles et efficaces du Game, les expériences les plus notables que nous avons eues et plein d'autres choses encore ...

